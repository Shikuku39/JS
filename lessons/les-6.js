// updating variable with its current value (shorthands)
let amount = 0

amount += 350 //amount = amount + 350
amount -= 350 //amount = amount - 350
amount *= 350 //amount = amount * 350
amount /= 350 //amount = amount / 350
amount %= 350 //amount = amount % 350
amount **= 350 //amount = amount ** 350
console.log(amount)     //0
